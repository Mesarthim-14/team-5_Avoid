//=======================================================================================
// 終了処理
//=======================================================================================
void CGame::Uninit(void)
{
	// プレイヤーの終了処理
	if (m_pPlayer)
	{
		m_pPlayer->Uninit();
		m_pPlayer = nullptr;
	}

	// デバッグ情報表示用フォントの破棄
	if (m_pFont)
	{
		m_pFont->Release();
		m_pFont = nullptr;
	}
}

