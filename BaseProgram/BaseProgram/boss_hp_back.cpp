#include "manager.h"
#include "renderer.h"
#include "game.h"
#include "boss_hp_back.h"
#include "texture.h"
#include "resource_manager.h"

//=============================================================================
// マクロ定義
//=============================================================================
#define SIZE    (D3DXVECTOR3(400.0f*2.0f, 45.0f*2.0f, 0.0f))
#define POS     (D3DXVECTOR3((SCREEN_WIDTH * 0.5f), SIZE.y*0.5f+8.0f, 0.0f))

//=============================================================================
// コンストラクタ
//=============================================================================
CBossHpBack::CBossHpBack(PRIORITY Priority) : CScene2D(Priority)
{
}

//=============================================================================
// デストラクタ
//=============================================================================
CBossHpBack::~CBossHpBack()
{
}

//=============================================================================
// インスタンス生成
//=============================================================================
CBossHpBack * CBossHpBack::Create()
{
    // メモリ確保
    CBossHpBack *pPolygon = new CBossHpBack(PRIORITY_UI);

    // !nullcheck
    if (pPolygon)
    {
        // 初期化処理
        pPolygon->Init();
        return pPolygon;
    }

    return nullptr;
}

//=============================================================================
// 初期化処理
//=============================================================================
HRESULT CBossHpBack::Init()
{
    // 初期化処理
    SetSceneInfo(POS, SIZE);
    CScene2D::Init();

    CTexture *pTexture = GET_TEXTURE_PTR;
    BindTexture(pTexture->GetTexture(CTexture::TEXTURE_NUM_BOSS_HP_BACK));

    return S_OK;
}

//=============================================================================
// 終了処理
//=============================================================================
void CBossHpBack::Uninit()
{
    CScene2D::Uninit();
}

//=============================================================================
// 更新処理
//=============================================================================
void CBossHpBack::Update()
{
    CScene2D::Update();
}

//=============================================================================
// 描画処理
//=============================================================================
void CBossHpBack::Draw()
{
}

void CBossHpBack::Render()
{
    CScene2D::Draw();
}
