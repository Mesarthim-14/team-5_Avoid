#include "key_e.h"
#include "manager.h"
#include "renderer.h"
#include "texture.h"
#include "resource_manager.h"

//=====================================================================
// マクロ定義
//=====================================================================
#define POS		(D3DXVECTOR3(800.0f,400.0f,0.0f))
#define SIZE	(D3DXVECTOR3(70.0f,70.0f,0.0f))

//=====================================================================
// コンストラクタ
//=====================================================================
CKeyE::CKeyE(PRIORITY priority):CScene2D(priority)
{
	m_isDraw = false;
}

//=====================================================================
// デストラクタ
//=====================================================================
CKeyE::~CKeyE()
{

}

//=====================================================================
// 生成処理
//=====================================================================
CKeyE * CKeyE::Create()
{
	CKeyE * pKeyE = new CKeyE(PRIORITY_UI);

	if (pKeyE)
	{
		pKeyE->SetSceneInfo(POS, SIZE);
		pKeyE->Init();

		return pKeyE;
	}
	return nullptr;
}

//=====================================================================
// 初期化処理
//=====================================================================
HRESULT CKeyE::Init()
{
	// テクスチャの設定
	CTexture *pTexture = GET_TEXTURE_PTR;
	BindTexture(pTexture->GetTexture(CTexture::TEXTURE_NUM_E_KEY));

	// 初期化処理
	CScene2D::Init();

	return S_OK;
}

//=====================================================================
// 描画処理
//=====================================================================
void CKeyE::Draw()
{
	if (m_isDraw)
	{
		CScene2D::Draw();
	}
}