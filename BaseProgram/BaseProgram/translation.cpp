//=====================================================================
// 生成処理
//=====================================================================
CTranslation * CTranslation::Create()
{
    CTranslation * pGauge = new CTranslation;

    if (pGauge)
    {
        pGauge->SetSceneInfo(POS, SIZE);
        pGauge->Init();
        return pGauge;
    }
    return nullptr;
}

