//=============================================================================
// コンストラクタ
//=============================================================================
CEnemy::CEnemy(PRIORITY Priority)
{
	m_nAttackInter = 0;
	m_nAttackCount = 0;
	m_nSTR = 0;
	m_fPower = 0.0f;
}

//=============================================================================
// デストラクタ
//=============================================================================
CEnemy::~CEnemy()
{
}

//=============================================================================
// 初期化処理
//=============================================================================
HRESULT CEnemy::Init()
{
	CCharacter::Init();

	return S_OK;
}

//=============================================================================
// 更新処理
//=============================================================================
void CEnemy::Update()
{
	CCharacter::Update();

	m_nAttackCount++;
	if (m_nAttackCount >= m_nAttackInter)
	{
		Attack();
		m_nAttackCount = 0;
	}
}

//=============================================================================
// 攻撃の情報設定
//=============================================================================
void CEnemy::SetAttackInfo(const int &nInter, const int &nSTR, const float &fPower)
{
	m_nAttackInter = nInter;
	m_nSTR = nSTR;
	m_fPower = fPower;
}

