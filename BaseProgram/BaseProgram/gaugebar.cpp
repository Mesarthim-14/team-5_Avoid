#include "gaugebar.h"
#include "manager.h"
#include "renderer.h"
#include "texture.h"
#include "resource_manager.h"

//=====================================================================
// マクロ定義
//=====================================================================
#define POS     (D3DXVECTOR3(80.0f,350.0f,0.0f))
#define SIZE    (D3DXVECTOR3(150.0f,450.0f,0.0f))

//=====================================================================
// コンストラクタ
//=====================================================================
CGaugebar::CGaugebar(PRIORITY priority) : CScene2D(priority)
{

}

//=====================================================================
// デストラクタ
//=====================================================================
CGaugebar::~CGaugebar()
{

}

//=====================================================================
// 生成処理
//=====================================================================
CGaugebar * CGaugebar::Create()
{
    CGaugebar * pGauge = new CGaugebar(PRIORITY_UI);

    if (pGauge)
    {
        pGauge->SetSceneInfo(POS, SIZE);
        pGauge->Init();
        return pGauge;
    }
    return nullptr;
}

//=====================================================================
// 初期化処理
//=====================================================================
HRESULT CGaugebar::Init()
{
    // テクスチャの設定
    CTexture *pTexture = GET_TEXTURE_PTR;
    BindTexture(pTexture->GetTexture(CTexture::TEXTURE_NUM_GAUGEBAR));
    CScene2D::Init();
    return S_OK;
}

//=====================================================================
// 終了処理
//=====================================================================
void CGaugebar::Uninit()
{
    CScene2D::Uninit();
}

//=====================================================================
// 更新処理
//=====================================================================
void CGaugebar::Update()
{
    CScene2D::Update();
}

//=====================================================================
// 描画処理
//=====================================================================
void CGaugebar::Draw()
{
    CScene2D::Draw();
}